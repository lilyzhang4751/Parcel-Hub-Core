CREATE TABLE parcel
(
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY      NOT NULL,
    station_code         varchar(50)                                  NOT NULL,
    waybill_code         varchar(20)                                  NOT NULL,
    pickup_code          varchar(32)                                  NOT NULL,
    shelf_code           varchar(64)                                  NOT NULL,
    recipient_name       varchar(32) NULL,
    recipient_mobile     varchar(32)                                  NOT NULL,
    status               int4        DEFAULT 0                        NOT NULL,
    notify_status        int4        DEFAULT 0                        NOT NULL,
    latest_inbound_time  timestamptz                                  NOT NULL,
    latest_outbound_time timestamptz DEFAULT '9999-12-31 23:59:59+00' NOT NULL,
    created_at           timestamptz DEFAULT CURRENT_TIMESTAMP        NOT NULL,
    updated_at           timestamptz DEFAULT CURRENT_TIMESTAMP        NOT NULL,
    CONSTRAINT parcel_pkey PRIMARY KEY (id)
);
CREATE UNIQUE INDEX uk_parcel_station_waybill_code ON parcel USING btree (station_code, waybill_code);
