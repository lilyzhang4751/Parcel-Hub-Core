-- =====================================================
-- message_outbox
-- =====================================================
CREATE TABLE message_outbox
(
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    event_id      varchar(64)                             NOT NULL,
    topic         varchar(128)                            NOT NULL,
    event_key     varchar(128)                            NOT NULL,
    payload       text                                    NOT NULL,
    status        varchar(32)                             NOT NULL,
    retry_count   int4        DEFAULT 0                   NOT NULL,
    last_error    varchar(500) NULL,
    next_retry_at timestamptz NULL,
    processing_at timestamptz NULL,
    created_at    timestamptz DEFAULT CURRENT_TIMESTAMP   NOT NULL,
    updated_at    timestamptz DEFAULT CURRENT_TIMESTAMP   NOT NULL,
    published_at  timestamptz NULL,
    CONSTRAINT message_outbox_pkey PRIMARY KEY (id),
    CONSTRAINT uk_message_outbox_event_id UNIQUE (event_id)
);
CREATE INDEX idx_outbox_status_next_retry ON message_outbox USING btree (status, next_retry_at);
-- Table Triggers
CREATE
OR REPLACE FUNCTION update_message_outbox_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at
= CURRENT_TIMESTAMP;
RETURN NEW;
END;
$$
LANGUAGE plpgsql;

CREATE TRIGGER trigger_message_outbox_updated_at
    BEFORE UPDATE
    ON message_outbox
    FOR EACH ROW
    EXECUTE FUNCTION update_message_outbox_updated_at();

