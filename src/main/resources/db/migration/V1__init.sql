CREATE TABLE waybill_registry
(
    id           int8 GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    waybill_code varchar(20)                           NOT NULL,
    station_code varchar(255)                          NOT NULL,
    status       int4        DEFAULT 0                 NOT NULL,
    created_at   timestamptz DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at   timestamptz DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_waybill_registry_station_code_not_blank CHECK ((char_length(btrim((station_code)::text)) > 0)),
    CONSTRAINT chk_waybill_registry_status CHECK ((status = ANY (ARRAY[0, 1, 2]))),
    CONSTRAINT chk_waybill_registry_waybill_code_not_blank CHECK ((char_length(btrim((waybill_code)::text)) > 0)),
    CONSTRAINT waybill_registry_pkey PRIMARY KEY (id)
);
CREATE UNIQUE INDEX uk_waybill_code_active ON waybill_registry USING btree (waybill_code) WHERE (status = 0);
